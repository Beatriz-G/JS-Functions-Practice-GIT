var welcome = function (name) {
    console.log('Welcome, ${name}. Have a great day!');
}

//arguments branch
welcome("vera");
welcome("Adrian");
welcome("Aida");

//parameters branch
var addTogether = function (num1, num2) {
    console.log(num1 + num2);
};

addTogether(13, 72);
addTogether(36, -2.88);
